class EjemploSwitchConStrings{
	public static void main(String[] args) {
		String opcion="tres";
		switch(opcion){
		case "cero":
			System.out.println("Caso Annie");
		break;
		case "uno": 
			System.out.println("Caso Isidora");
		break;
		case "dos": 
			System.out.println("Caso Chivola");
		break;
	default:
		System.out.println("Caso a la que venga");
		}
	}
}
